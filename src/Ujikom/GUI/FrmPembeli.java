    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        String KTP = xKTP.getText();
        String Nama = xNama.getText();
        String Alamat = xAlamat.getText();
        String Telepon = xTelepn.getText();
        if(KTP.isEmpty()||Nama.isEmpty()||Alamat.isEmpty()||Telepon.isEmpty()){
             JOptionPane.showMessageDialog(null, "Data Tidak Boleh Kosong!");
        }else{
            try{
            String sql = "INSERT INTO pembeli VALUES("+
                         "'"+xKTP.getText()+"',"+
                         "'"+xNama.getText()+"',"+
                         "'"+xAlamat.getText()+"',"+
                         "'"+xTelepn.getText()+"')";
            koneksi.cn.executeUpdate(sql);
            JOptionPane.showMessageDialog(null,xKTP.getText()+" Disimpan!");
            bersih();
            nonaktif();
            tampil();
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
        }
    }//GEN-LAST:event_simpanActionPerformed

    private void ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahActionPerformed
        // TODO add your handling code here:
        int app;
        if((app= JOptionPane.showConfirmDialog(null, "Apakah anda yakin akan mengubah "+xKTP.getText()+"?","Ubah",JOptionPane.YES_NO_OPTION))==0){
            try{
                String sql = "UPDATE pembeli SET "+
                             "Nama_Pembeli ='"+xNama.getText()+"',"+
                             "Alamat_Pembeli ='"+xAlamat.getText()+"',"+
                             "Telp_Pembeli ='"+xTelepn.getText()+"' "+
                             "WHERE KTP ='"+xKTP.getText()+"'";
                koneksi.cn.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, xKTP.getText()+" Diubah!");
                bersih();
                nonaktif();
                tampil();
           }catch(Exception e){
                System.out.println(e.getMessage());
           }
        }
    }//GEN-LAST:event_ubahActionPerformed

