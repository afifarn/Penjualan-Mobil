    public void tampil(){
        int baris = jTable1.getRowCount();
        for(int i = 0;i<baris;i++){
            tabelmodel.delete(0, baris);
        }
        try{
        String sql = "select * from pembeli";
        koneksi.rs = koneksi.cn.executeQuery(sql);
        while(koneksi.rs.next()){
            TabelPembeli beli = new TabelPembeli();
            beli.setKTP(koneksi.rs.getString(1));
            beli.setnama(koneksi.rs.getString(2));
            beli.setalamat(koneksi.rs.getString(3));
            beli.settelepon(koneksi.rs.getString(4));
            tabelmodel.add(beli);
        }
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
