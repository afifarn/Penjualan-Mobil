    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        String kode = xkode.getText();
        String harga = xharga.getText();
        String uang = xuang.getText();
        String jumlah = xjumlah.getText();
        String bunga = xbunga.getText();
        String nilai = xnilai.getText();
        if(kode.isEmpty()||harga.isEmpty()||uang.isEmpty()||jumlah.isEmpty()||bunga.isEmpty()||nilai.isEmpty()){
            JOptionPane.showMessageDialog(null, "Data Tidak Boleh Kosong!");
        }else{
            try{
            String sql = "INSERT INTO paket_kredit VALUES("+
                         "'"+xkode.getText()+"',"+
                         "'"+xharga.getText()+"',"+
                         "'"+xuang.getText()+"',"+
                         "'"+xjumlah.getText()+"',"+
                         "'"+xbunga.getText()+"',"+
                         "'"+xnilai.getText()+"')";
            koneksi.cn.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, xkode.getText()+" Disimpan!");
            bersih();
            nonaktif();
            tampil();
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
        }
    }//GEN-LAST:event_simpanActionPerformed

