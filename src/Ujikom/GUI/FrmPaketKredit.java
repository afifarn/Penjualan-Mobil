    public void tampil(){
        int baris = jTable1.getRowCount();
        for(int i = 0;i<baris;i++){
            tabelmodel.delete(0, baris);
        }
        try{
        String sql = "select * from paket_kredit";
        koneksi.rs = koneksi.cn.executeQuery(sql);
        while(koneksi.rs.next()){
            TabelPaket paket = new TabelPaket();
            paket.setkode(koneksi.rs.getString(1));
            paket.setharga(koneksi.rs.getString(2));
            paket.setuang(koneksi.rs.getString(3));
            paket.setjumlah(koneksi.rs.getString(4));
            paket.setbunga(koneksi.rs.getString(5));
            paket.setnilai(koneksi.rs.getString(6));
            tabelmodel.add(paket);
        }
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    public void kdauto(){
        try{
            String sql = "select max(right(kode_paket,2)) from paket_kredit";
            koneksi.rs = koneksi.cn.executeQuery(sql);
            while(koneksi.rs.next()){
                if(koneksi.rs.first()==true){
                    koneksi.rs.last();
                    int hitung = koneksi.rs.getInt(1)+1;
                    String itung = String.valueOf(hitung);
                    int panjang = itung.length();
                    for(int i=0;i<2-panjang;i++){
                        itung = "00"+hitung;
                    }
                    xkode.setText("P-"+itung);
                }else{
                    xkode.setText("P-001");
                }
            }
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
