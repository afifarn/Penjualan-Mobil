    private void bayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bayarActionPerformed
        // TODO add your handling code here:
        int a = Integer.parseInt(sisahutang.getText());
        int b = Integer.parseInt(harusdibayar.getText());
        int c = a - b;
        try{
        String sql = "UPDATE bayar_cicilan SET "+
                     "tgl_cicilan = '"+Tanggal.getText()+"',"+
                     "jumlah_cicilan = '"+jumlahcicilan.getText()+"',"+
                     "cicilan_ke = '"+cicilanke.getText()+"',"+
                     "cicilan_sisa_ke = '"+sisacicilan.getText()+"',"+
                     "cicilan_sisa_harga = '"+c+"' "+
                     "WHERE kode_cicilan = '"+Transaksi.getText()+"'";
        koneksi.cn.executeUpdate(sql);
        JOptionPane.showMessageDialog(null, "Transaksi Berhasil");
        }catch(Exception e){
            System.out.println();
        }
        try{
        File file = new File("src/Ujikom/Report/reportBayar.jrxml");
        jasperdesain = JRXmlLoader.load(file);
        param.put("notrans", Transaksi.getText());
        param.put("harusdibayar", harusdibayar.getText());
        param.put("dibayar", dibayar.getText());
        param.put("kembalian", kembalian.getText());
        jasperreport = JasperCompileManager.compileReport(jasperdesain);
        jasperprint = JasperFillManager.fillReport(jasperreport, param,koneksi.conn);
        JasperViewer.viewReport(jasperprint,false);
        bersih();
        bayar.setEnabled(false);
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_bayarActionPerformed

