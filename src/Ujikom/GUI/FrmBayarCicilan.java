    private void SD(){
        try{
            koneksi.rs = koneksi.cn.executeQuery("select * from kredit where kode_kredit = '"+kodekredit.getSelectedItem()+"'");
            while(koneksi.rs.next()){
                String ktp = koneksi.rs.getString(2);
                String kode_paket = koneksi.rs.getString(3);
                String kode_mobil = koneksi.rs.getString(4);
                noktp.setText(ktp);
                kodepaket.setText(kode_paket);
                kodemobil.setText(kode_mobil);
                koneksi.rs.close();
                koneksi.rs = koneksi.cn.executeQuery("select * from pembeli where ktp ='"+ktp+"'");
                while(koneksi.rs.next()){
                    Nama.setText(koneksi.rs.getString(2));
                    Alamat.setText(koneksi.rs.getString(3));
                    NoTelepon.setText(koneksi.rs.getString(4));
                    koneksi.rs.close();
                    koneksi.rs = koneksi.cn.executeQuery("select * from paket_kredit where kode_paket ='"+kode_paket+"'");
                    while(koneksi.rs.next()){
                    jumlahcicilan.setText(koneksi.rs.getString(4));
                    nilaicicilan.setText(koneksi.rs.getString(6));
                    harusdibayar.setText(koneksi.rs.getString(6));
                    koneksi.rs.close();
                        koneksi.rs = koneksi.cn.executeQuery("select * from mobil where kode_mobil ='"+kode_mobil+"'");
                        while(koneksi.rs.next()){
                        MerkMobil.setText(koneksi.rs.getString(2));
                        TypeMobil.setText(koneksi.rs.getString(3));
                        WarnaMobil.setText(koneksi.rs.getString(4));
                        HargaMobil.setText(koneksi.rs.getString(5));
                    }
                }
            }
            }
        }catch(Exception e){
         JOptionPane.showMessageDialog(null, "Ada Kesalahan");
         System.out.println(e.getMessage());            
        }
    }
