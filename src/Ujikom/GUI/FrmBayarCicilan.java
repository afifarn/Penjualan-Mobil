    private void cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariActionPerformed
        // TODO add your handling code here:
        try{
            String s = "select * from bayar_cicilan where kode_cicilan = '"+Transaksi.getText()+"'";
            koneksi.rs = koneksi.cn.executeQuery(s);
            int baris = 0;
            int cicilan = 0;
            int sisa = 0;
            int hutang = 0;
            String kode = null;
            while(koneksi.rs.next()){
                baris = koneksi.rs.getRow();
                kode = koneksi.rs.getString("kode_kredit");
                cicilan = koneksi.rs.getInt("cicilan_ke")+1;
                sisa = koneksi.rs.getInt("jumlah_cicilan")-1;
                hutang = koneksi.rs.getInt("cicilan_sisa_harga");
            }
            if(baris==1){
                kodekredit.setSelectedItem(kode);
                cicilanke.setText(String.valueOf(cicilan));
                sisacicilan.setText(String.valueOf(sisa));
                sisahutang.setText(String.valueOf(hutang));
                bayar.setEnabled(true);
            }else{
                bersih();
                Transaksi.requestFocus();
                JOptionPane.showMessageDialog(null, "Data Tidak Ditemukan!");
            }
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_cariActionPerformed

